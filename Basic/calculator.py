#taking number1
number1 = int(input('Enter a number '))

#taking sign
sign = input('Enter sing : ')

#taking number 2
number2 = int(input('Enter a number '))

# add then
if sign == '+':
    print(number1 + number2)

if sign == '-':
    print(number1 - number2)

if sign == '*':
    print(number1 * number2)

if sign == '/':
    print(number1 / number2)